//------------------------------------------------------------------------
ExternalFontHUD::ExternalFontHUD(FontProviderHUD *provider, const char* name,
                                 unsigned fontFlags)
    : Font(fontFlags),
      pFontProvider(provider)
{
    Name.Resize(SFstrlen(name) + 1);
    SFstrcpy(&Name[0], SFstrlen(name) + 1, name);
    SetFontMetrics(0.f, 930.f, 1024.f-930.f);
    Advance = 6.0f / (13.0f / ExternalFontHUD::GetNominalGlyphHeight());
}


//------------------------------------------------------------------------
int ExternalFontHUD::getGlyphIndex(UInt16 code)
{
    if (code >= 32 && code <= 127) 
        return code - 32;

    int end = sizeof(RasterFont6x13_Header) / 2 - 1;
    int beg = 0;
    while(beg <= end)
    {
        int  mid = (end + beg) / 2;
        unsigned chr = RasterFont6x13_Header[mid];
        if (chr == code)
        {
            return mid + 128 - 32;
        }
        else
        {
            if (code < chr) end = mid - 1;
            else            beg = mid + 1;
        }
    }
    return 127-32;
}

//------------------------------------------------------------------------
int ExternalFontHUD::GetGlyphIndex(UInt16 code)
{
    return getGlyphIndex(code);
}

//------------------------------------------------------------------------
bool ExternalFontHUD::GetGlyphRaster(unsigned glyphIndex, unsigned hintedSize, GlyphRaster* raster)
{
    SF_UNUSED(hintedSize);

    unsigned w = RasterFont6x13_Metric.W;
    unsigned h = RasterFont6x13_Metric.H;
    raster->Width   = w;
    raster->Height  = h;
    raster->OriginX = 0;
    raster->OriginY = int(RasterFont6x13_Metric.BaseLine);
    raster->Raster.Resize(w * h);

    for(unsigned y = 0; y < h; ++y)
    {
        UByte scanline = RasterFont6x13_Bitmap[glyphIndex * h + y];
        UByte* dst = &raster->Raster[y * w];
        for(unsigned x = 0; x < w; ++x)
        {
            *dst++ = (scanline & 0x80) ? 255 : 0;
            scanline <<= 1;
        }
    }
//memset(&pRaster->Raster[0], 127, w*h); //DBG
    return true;
}

